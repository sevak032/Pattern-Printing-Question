#include<iostream>
using namespace std;
int main (){

int m; // m = rows
cout<<"Enter The Number Of Rows : ";
cin>>m;



for(int i = 1;  i<=m;  i++){            // m = Rows
    for( int j = 1;  j<=m;  j++){       // n = Column
        cout<<(char) (j + 64)<<" "; // print any things     //(char) = type casting
    }
    cout<<endl;
}

}



